                    <h3 class = "has-line">What is the FIRST Robotics Competition?</h3>

                      <p>FIRST organized in 1992, the FIRST Robotics Competition (FRC) is an annual competition in which high school teams of 25 students or more compete to build/program robots that perform set tasks against other opponents. Teams have less than 6 weeks to build a robot that not only meets stated requirements, but performs effectively against robots built by other high schools. FRC Teams are not only expected to build excellent robots, but also to raise their own funds and help introduce their local community and fellow students to the world of robotics as well. Through participation in the First Robotics Competition, students enhance their critical-thinking and teamwork skills and apply mathematics and engineering skills they learned in school to real-world situations.</p>
