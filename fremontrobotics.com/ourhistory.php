                    <p>Our origins may be traced back to 2009, when four high schoolers founded Fremont High School's first robotics team, VEX Team 1381, the "PyroChickens". The following year, our team decided to shift gears to the First Robotics Competition. Occupying a small corner of the school’s Engineering Classroom, we, now the “Fremont Grinders", did very well for a rookie team, becoming the highest rookie seed. Our robot, the <i>Phoenix-1</i>, won the team runner-up position for the Inventor’s Award at Silicon Valley Regional.</p>

<p>For the 2012 FRC Season, we, with now over 20 members, moved to a small shed just behind the Fremont Union High School District office. Proud of our contraption, named <i>Phoenix-2</i>, the Fremont Grinders would go on to become a semifinalist at Central Valley Regional.</p>

<p>In 2013, our team, now called the “Fremont High Firebirds”, built <i>Oddjob</i>, named after the famed James Bond villain, for the Ultimate Ascent game. It did very well at the Central Valley Regional, and we not only became the #1 seed, but also won second place and the Judges award. We began participating in more outreach events, showcasing our robot at Maker Faire 2013. Later in the postseason, the team would host Calgames 2013 and place there as a Quarterfinalist.</p>

<p>We moved once again to a new portable classroom during the summer of 2013. Membership grew to over 40 active members, and we began focusing on using more professional tools, such as CAD and Quality Assurance, to produce an exceptional robot. Our 2014 Robot, the <i>Companion Cube</i>, became a semifinalist at Central Valley Regional that year. Enthusiasm and spirit blossomed within our team, and consequently we won the Spirit Award at CVR as well. In May 2014, we voted to change our name to the “Fremont High Firebots” to reflect our dedication to fostering a passion for technology.</p>

<p>Now in its 5th year of FRC, there are over 55 active members in the Fremont High Firebots, of which 30% are female. We have been busy improving the quality of have robot: 2015 marks the first year the team’s robot, <i>Spark</i>, has been completely built in CAD. We attend many local community events, such as the Silicon Valley SteamFest and Maker Faire, and visit local schools, working to inspire our local community and extend knowledge about FIRST programs. At this point, it’s not just about the competition anymore.  With over 5 years of experience, we hope to inspire generations more of future scientists, businessmen, and engineers.</p>
