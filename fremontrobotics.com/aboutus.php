                                <h4><a href="#">Our Mission</a></h4>
                                <div>
                                    
                            <p>The Fremont High Firebots strive to not only teach high school students proper engineering techniques and enhance their leadership and decision-making skills through participation in the First Robotics Program, but also to inspire younger students by introducing them to opportunities in STEM fields that they may otherwise not be exposed to.</p>
                            <p>Based on research conducted by For Inspiration and Recognition of Science and Technology (FIRST; learn more <a href = "aboutfirst.php"  target = "_blank">here</a>), the chances of having a successful career in engineering, science, technology, and business are very high for people who apply themselves in school.  Studies have shown that FIRST Robotics participants are:</p>

                            <ul>
                            <li>Twice as likely to pursue a career in science and technology</li>
                            <li>Four times as likely to pursue a career in engineering</li>
                            <li>More than twice as likely to volunteer in their communities</li>
                            </ul>
                                </div>
                                
                                <!-- Accordion Item 2 -->
                                <h4><a href="#">Our Organization</a></h4>
                                <div>
                                    <p>Team 3501 greatly supports the ideal of a student-driven team.  We are proud of having the student body design, build, and test the robot instead of the mentors.  The Firebots value student leadership, initiative, teamwork, and respect within the team as well as among the local FIRST teams. To ensure that the team runs smoothly and efficiently, the Firebots are split into subgroups that are managed by a Student Leadership Council. The subgroups are categorized into technical and non-technical. At the beginning of the training season, students may choose one technical subgroup (Mechanical, Electrical, Software) and/or one non-technical subgroup (Strategy, Finance, PR/Marketing, Safety, Special Projects) so that they achieve mastery within their subgroup.</p>

                            <p>As a non-profit organization, the Fremont Robotics Team relies on community support.  In return, team members give back to the community by participating in robot demonstrations at local schools and companies, thereby developing an interest in science and technology in young minds.</p>
                                </div>
                                
                                <!-- Accordion Item 3 -->
                                <h4><a href="#">Our Leadership</a></h4>
                                <div class = "row">
                                <div class = "large-6 columns">
                            <p>Student leads are nominated before elections by fellow teammates selected through a voting process. Candidates must give a 2-5 minute speech or video followed by participating in a Q&A session during elections. In the case of a tie, or if a student wins a position within two votes, mentors decide the winner of the election.<br/><br/>All leads are expected to:<ul>
<li>coordinate projects within their subgroup.</li>
<li>meet with other leads to coordinate robot and team activities.</li>
<li>manage conflicts within their subgroup.</li>
<li>organize technical education for members within their subgroup.</li></ul>
<p>This structure ensures that the team runs efficiently while still remaining student-run. For more information about our leadership, click <a href = "ourleadership.php"  target = "_blank">here.</a> </p>
                            </div>
                            <div class = "large-6 columns">
