                    <h3 class = "has-line">What is FIRST?</h3>

                    <p>FIRST (For Inspiration and Recognition of Science and Technology) is a non-profit organization dedicated to introduce students to prospective careers in science and technology. In their own words, their mission statement is “to show students of every age that science, technology, and problem-solving are not only fun and rewarding, but are proven paths to successful careers and a bright future for us all.” Founded by Dean Kamen in 1990, the organization has brought millions of students into the realm of robotics over the past two decades. </p>
                    <p>Amongst the programs they run include:
                    <ul>
                    <li>FIRST Junior Lego League: Grades K - 3 (ages 6 – 9)</li>
                    <li>FIRST Lego League: Grades 4 - 8 (ages 9 – 14)</li>
                    <li>FIRST Tech Challenge: Grades 9 – 12 (ages 14 – 18)</li>
                    <li>FIRST Robotics Competition: Grades 9 – 12 (ages 14 – 18)</li>
                    </ul></p>
                    <p>Part of the unique atmosphere and environment of the FIRST Robotics Tournament is created in part due to the special values FIRST instructs their teams to uphold. These values help ensure a helpful and positive environment that encourages teamwork and respect within FIRST Robotics Competition teams. For more information about these values, please visit this page <a href = "http://www.firstinspires.org/about/vision-and-mission" target = "_blank">here.</a></p>
      
      <h4>Gracious Professionalism</h4>
      
      <p>FIRST states that “Gracious professionalism… is the way of doing things that encourages high-quality work, emphasizes the value of others, and respects individuals and the community”. Gracious professionalism is one of the core values of the Team 3501 environment.</p>

    <h4>Coopertition</h4>
      
      <p> Coopertition is, according to the FIRST website, “displaying unqualified kindness and respect in the face of fierce competition”. Here at team 3501, we strive to display coopertition to fellow robotics teams.</p>
