                            <h4><a href="#">Our Training</a></h4>
                                <div class = "row">
			    <div class = "large-6 columns">
                            <p>Using a badge system designed in-house, our team trains all returning and rookie members. Members attend training sessions led by team members, where they learn the skills and tools neccessary to construct the robot come build season. After learning their skills, they must be tested on their ability to apply this knowledge in order to receive their badge. In order to make it onto the travel team for competition, members must have acquired the needed badges in their subgroup as well as mandatory badges (Game knowledge, First Aid, etc.).<br /><br />To learn more about the Team 3501 training website, please visit the website <a href = "http://app.fremontrobotics.com" target="_blank">here</a>.</p>
