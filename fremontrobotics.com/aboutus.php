                            <h4><a href="#">Our Seasons</a></h4>
                                <div class = "row">
			    <div class = "large-6 columns">
                            <p>The team uses a year - round approach to ensure that team members receive the education they deserve while also fulfilling the mission of FIRST. <br /><br /> Students engage in a <strong>Training Season</strong> from September through December, during which they participate in workshops taught by experienced mentors and senior students. These workshops include technical skill development in mechanical engineering, electrical engineering, so"ware development, safety, game strategy, financial management, public relations, and media. <br /><br />During <strong>Build Season</strong>, from January through March, the team develops a robot in preparation for competition. <br /><br />The <strong>Competition Season</strong> runs from March through April. Team members who wish to participate in Competition Season must have been active during the Training and Build Seasons. <br /><br />Competition Season is followed by the <strong>Outreach Season</strong> in May through August. During Outreach Season, team members participate in several community events, providing robot demonstrations at local schools, libraries and companies, thereby developing an interest in science and technology in young minds.</p>
                            </div>
                            <div class = "large-6 columns">
                            <img src = "images/calendar.png">
